public class NotesRecursive2 {
    public static void main (String[] args){
        int math = 17;
        System.out.println(math);
        math = math + 5;
        System.out.println(math);
        math = math - 4;
        System.out.println(math);
        math = math * 2;
        System.out.println(math);
        math = math / 4;
        System.out.println(math);
        math = math % 3;
        System.out.println(math);
    }
}