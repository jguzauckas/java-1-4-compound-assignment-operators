public class NotesIncDec1 {
    public static void main (String[] args){
        int count = 5;
        System.out.println(count);
        count = count + 1;
        System.out.println(count);
        count = count - 1;
        System.out.println(count);
        count += 1;
        System.out.println(count);
        count -= 1;
        System.out.println(count);
    }
}
