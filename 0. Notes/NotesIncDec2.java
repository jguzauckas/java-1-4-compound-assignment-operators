public class NotesIncDec2 {
    public static void main (String[] args){
        int count = 5;
        System.out.println(count);
        count += 1;
        System.out.println(count);
        count -= 1;
        System.out.println(count);
        count++;
        System.out.println(count);
        count--;
        System.out.println(count);
    }
}
